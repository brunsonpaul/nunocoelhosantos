        			<p><span class="italic">On the Edge</span> presents a sobering and often unnerving vision of the urban landscape by examining Siem Reap. It is one of Cambodia’s fastest growing cities – filled with hotels, shops, markets as well historical landmarks, all of which fuel its tourist industry and account for its increasing number of developments.</p>
        			<p>However, in this collection there is little visual reference to these sites and instead viewers are exposed to another side to the region. Away from the hordes of tourists and hectic commerce, along the outer edge and in and around the Siem Reap lie natural, dry, unkempt grasslands, which remain untouched by these major developments.</p>
