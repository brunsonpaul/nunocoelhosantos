        			<span class="italic">Amulet World</span> is a study of Buddhism and its interaction with the rise of consumerist culture, focusing specifically on the Buddhist amulet trade in Thailand. This collection exposes the duality and tension between spiritual faith and capitalist ventures, presenting a sobering yet often unnerving reality of how significantly amulets play a role in the lives of many people living in Thailand.</p>
